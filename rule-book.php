						     <td>The Member of Government opens the second half of the debate, and should.
							 <ul class="objectives"><li><b>Refute.</b> Address any new contribution(s) from the DLO. Consider engaging in holistic refutation of the OO, or even preempting what the CO is likely to claim.</li>
<li><b>Offer an "extension".</b> Add something new. You might choose to present an entirely new argument, or you might opt to develop an important argument that the OG underdeveloped.</li>
<li><b>Explain how the CG's position fits into the debate.</b> Avoid contradicting the OG case. Explain why what you're adding is important <i>in relation to</i> the OG case.</li>
							 </ul>
							 </td>
							 
						     <td>The Government Whip should
							 <ul class="objectives"><li><b>Identify the 2-3 most relevant concepts in the debate.</b> These concepts should serve as the main points of your speech. At some point, explain why your conceptual breakdown is the best way to view the debate.</li>
<li><b>Sell the "extension".</b> Demonstrate how your team's material relates to other important content in the debate. Articulate why your position defeats the most important arguments presented by the Opposition.</li>
<li><b>Refute.</b> Answer the contribution made by the Member of Opposition. Engage in holistic refutation of the Opposition.</li>
<li><b>Avoid making new arguments.</b> You may, however, add details or examples in support of previous claims.</li>
							 </ul>
