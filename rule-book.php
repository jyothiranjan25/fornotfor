			<li>The speaking time for each speaker shall be a total of 5 minutes.</li>
			<li>Points of Information or any kind of interruption will not be allowed during the rounds.</li>
			<li>All rounds are presided by a single judge bench/chair.  After the conclusion of all speeches, the chair asks the participants to leave the room. The chair then spends a few minutes deciding the debate. Each team is given a ranking (first place, second place, third place, fourth place). Each speaker is assigned a speaker score on a scale of 0-100. When the decision is finalized, the judge invites the debaters back into the room. The decision is announced and the chair provides some reasoning to support the rankings.</li>

			<li>All speeches shall be ranked and an aggregate score of both the speakers leads to team ranking.</li>
			<li>The top 32 teams then, shall progress to the Pre-Quarter Finals. From the Pre-Quarter Finals, the top 16 ranked teams shall progress to the Quarter Finals. From the Quarter Finals, the top 8 ranked teams shall progress to the Semi Finals. From the Semi Finals,  the top 4 ranked teams shall progress to the Finals. The top ranked team shall be the Winner and the 2nd ranked team shall be the Runners-up of the Tournament. </li>
			<li><b>All teams must mandatorily be present in the Vijay Bhoomi University Campus for the qualifying rounds.</b></li>

        </ul>
		<h3>Software Guidelines :</h3> 
		<ul class="objectives">
		     <li>The tournament shall take place on the Zoom platform. All the relevant links to access and join the session shall be provided to the official email IDs of the participants and will also be shared via WhatsApp.</li>	
			 <li>All Communications shall be conveyed via WhatsApp and Facebook. The official announcements and motion release for the rounds will all be done through the official WhatsApp group of the tournament.</li>
			 <li>It is mandatory for the participants to share their video along with their audio during the rounds, so it is requested that a device with a functional camera and microphone is used. Only in extraordinary circumstances would video sharing not be mandatory.</li>
		</ul>
		<h3>Procedure of Rounds :</h3> 
		<ul class="objectives">
		     <li>The motions/topics for each debate shall be <b>released two days</b> in advance to the date of the debate. All queries shall be entertained before the tournament dates and no queries shall be entertained once the tournament is underway.</li>	
			 <li>Pairing and matchups of teams will be done by the organizers and shall be released a few days prior to the event so that all participants are aware of their Zoom Track and Round Timings.</li>	
			 <li>The links for the rounds shall be shared on the WhatsApp groups during the Opening Ceremony of the Competition along with additional rules.</li>	
			 <li>Participants are requested to join their respective Zoom calls at least 15 minutes prior to the beginning of their rounds. The participants can use these 15 minutes as part of their prep time.</li>	
			 <li>All participants and judges shall be waiting in their respective Zoom calls. If any participant does not join on the given time, the judge may choose to wait for an additional length of 5 minutes at most and get in touch with their moderator to decide further proceedings.</li>	
			 <li>In case of absence of one participant from a team, the remaining participant will be allowed to proceed as the Iron Person, i.e. one speaker will be allowed to make both speeches on behalf of the team. In such a case, the Iron Person participant will be marked on the speech with the higher score (for the purpose of individual scoring), while both the speeches will be marked (for the purpose of team scoring).</li>	
			 <li>Once the debate ends, the judges shall submit their scores, evaluation and final remarks through the forms circulated to them prior to the commencement of the rounds. Judges will deliver constructive feedback to the participants, without disclosing the results.</li>	
			 <li>Dress Code for Participants: Formals/School Uniforms</li>	
			 <li>If judges believe that debaters have exceeded the time repeatedly, they can stop/ disqualify the debater and factor this misconduct into their decision.</li>	
			 <li>Participants are expected to strictly adhere to the time limits provided. Exceeding the time limit would incur penalties on scores. Moderators will prompt the participants on speech timing intervals in the chat box.</li>	
			 <li>Observers shall always remain on mute and will not be allowed to speak, chat, record any speech, send documents or use their video during the course of the debate.</li>	
			 <li>Speakers may record their own speeches. However, speakers shall not record the other speaker in the debate without the consent of the other speaker.</li>	
			 <li>All participants are required to be on ‘mute’ at all times unless they are called upon to speak.</li>
		</ul>
		<h3>Equity Policy :</h3> 
		<p class="mb-0">Any behaviour that attempts to exclude someone from meaningful participation in a debate or the tournament as a whole is not appreciated at this tournament. This includes, but is not limited to:</p>
		<ul class="objectives">
		     <li>Bullying: Bullying is the repeated, unreasonable behaviour by an individual or group, directed towards another individual or group, either physical or psychological in nature, that intimidates, oﬀends, degrades, humiliates, undermines and/or threatens. This includes pressuring another individual or group to do something that they are uncomfortable with.</li>	
			 <li>Direct Discrimination: Direct discrimination is treating another individual or group less favourably on the basis of a protected attribute than someone without that attribute in the same circumstances or circumstances not materially diﬀerent. Protected attributes include, but are not limited to; age, appearance, disability, ethnicity, gender, nationality, personal background, religion, sexuality and social status.</li>	
			 <li>Harassment: Harassment is any unwelcome, oﬀensive, abusive, belittling or threatening behaviour that humiliates, oﬀends or intimidates an individual or group on the basis of any part of their identity. Note that sexual harassment has a speciﬁc meaning as any unwelcome sexual advance, request for sexual favours or any other unwelcome conduct of a sexual nature that humiliates, oﬀends or intimidates a person and which a reasonable person, having regard to all the circumstances, would anticipate making the person humiliated, oﬀended or intimidated.</li>	
			 <li>Offensive language: Sometimes words can hurt a lot regardless of the intention behind these words. Therefore it is import to avoid language that can attack someone’s identity. This includes but is not limited to – gender, sex, sexual orientation, sexuality, disability, religion, race, nationality, ethnicity, educational background, age, appearance, class or debating skills. Furthermore, keep in mind that some topics such as violence and sexual assault can be painful for some people. Therefore, avoid overly graphic language and be extra careful with the words you pick when talking about sensitive topics.</li>	
			 <li>Causing Disturbances: Any online disturbance done with intention to throw oﬀ another speaker or create an unpleasant debating room shall result in the issuance of warnings. Upon receipt of a third warning, the organisation committee is entitled to expel the participant from the competition without any recognition (including but not limited to participant certiﬁcates).</li>	
			 <li>Undermining the equity policy: Attempts to undermine and/or belittle the equity policy, or parts of it, will also be an equity violation.</li>	
			 <li>Victimisation: Victimisation means to cause detriment to a person because that person has made a complaint or taken part in complaints proceedings.</li>	
		</ul>
		<p>Any other form of behaviour that can reasonably make people feel unwelcome and/or unsafe at this tournament will also fall under equity violations and shall be based on the discretion of all policies shall be adhered to by the participants throughout all days of the tournament and the power vests in the organisation committee to make any decision based on the policies placed herein.</p>
		
