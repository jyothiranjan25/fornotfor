			<p class="mb-0">The Government opens the debate, and the Opposition delivers the speech. Each Speaker presents one 7-minute speech. The speeches proceed in this order :</p>
			
		    <ul class="objectives">
			     <li>Prime Minister</li>
			     <li>Leader of Opposition</li>
			     <li>Deputy Prime Minister</li>
			     <li>Deputy Leader of Opposition</li>
			     <li>Member of Government</li>
			     <li>Member of Opposition</li>
			     <li>Government Whip</li>
			     <li>Opposition Whip</li>
			</ul>
			</li>
			<li>The speakers speak in rotation, beginning with the first team member of 1st Proposition (“the Prime Minister”). They are followed by the opening speaker for 1st Opposition (“Leader of the Opposition”), who in turn is followed 1st Proposition's second speaker (“the Deputy Prime Minister”), who is followed by 1st Opposition's second speaker (“Deputy Leader of Opposition”), who is followed by 2nd Proposition's first speaker (“Member of Government”), who is followed by 2nd Opposition's first speaker (“Member of Opposition”), who is followed by 2nd Proposition's second speaker (“Government Whip”), who is followed by 2nd Opposition's second speaker (“Opposition Whip”).<br> <br>
			<h3 style="text-align:center;">British Parliamentary (PB) Debate</h3>
			<div class=" table-responsive">
			     <table class="table table-bordered table-striped">
				     <thead>
			             <tr>
						     <td colspan="2" style="text-align:center;"><b>Opening Government (OG)</b></td>
						 </tr>	 
