						 <tr>
						     <td>The Leader should oppose the motion and the case presented by the PM.
							 <ul class="objectives">
							 <li><b>Refute the PM's case.</b> Be specific. Criticize the case you've just heard.</li>
							 <li><b>Oppose.</b> Why is the motion itself problematic? State your team's position and provide reasoning to support it. As you prepare for this speech, consider that the motion might possibly do more harm than good, foreclose better alternatives, or be tethered to a destructive worldview.</li>
							 <li><b>Recognize that the first two tasks on this list are not always separate.</b> A good PM speech will allow you to oppose both at the same time.</li>
							 </ul>
							 </td>
						     <td>The DLO concludes the first half of the debate for the Opposition, and should
							 <ul class="objectives"><li><b>Rebuild.</b> Defend your partner's case from the DPM's refutation. Reiterate the key elements of your team's position</li>
<li><b>Refute.</b> Address new, relevant material presented by the DPM. Illustrate any importatnt tension(s) between the PM & DPM speeches. Highlight LO refutation that was ignored or insufficiently covered by the DPM.</li>
<li><b>Add Depth.</b> Add something to your team's case. You can provide additional examples, explanation, or analysis to support  a previous claim. You can present a new argument.</li>
							 </ul>
							 </td>
