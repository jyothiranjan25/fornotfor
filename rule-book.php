						     <td>The job of the PM is to present a debatable, persuasive case in support of the motion. To this end, a speaker should :
							 <ul class="objectives">
							     <li><b>Define the motion.</b> This might include: defining specific terms in the motion advocating specific policy change(s), and/ or articulating the roles of relevant stakeholders.</li>
								 <li><b>provide a complete case.</b>  Establish that a problem exists, and provide reasons why your team's advocacy resolves it. If necessary, signpost the new material your partner will add.</li>
								 <li><b>Avoid being too clever.</b>  Aim to set up a <i>debatable</i> case that supports the most obvious interpretation of the motion.</li>
							 </ul>
							 </td>
						     <td>The DPM closes for the Opening Government, and should :
							 <ul class="objectives"><li><b>Rebuild.</b> Defend your team's case by answering the LO's refutation. Reiterate the key elements of your team's position.</li>
							 <li><b>Refute the LO's case.</b> Be specific. Criticize what you've just heard Compare it to your team's position.</li>
							 <li><b>Follow through on promises.</b> If your partner declared that you would present new material in support of your team's position, do so. At the very least, add depth to the original case by providing additional deaths, examples, or explanation.</li>
							 </ul>
							 </td>
			              </tr>
			         </tbody>
			    
