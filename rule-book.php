<li><b>Make a contribution.</b> Add something new. You might choose to present a new argument or to further develop an OO argument.</li>
<li><b>Explain how the CO's contribution fits into the debate.</b> Illustrate why your position is important <i>in relation to</i> the OO case. Avoid contradicting the OO if you can.</li>
							 </ul>
							 </td>
							 
						     <td>The Opposition Whip closes the debate, and should : 
							 <ul class="objectives"><li><b>Identify the 2-3 most relevant concepts in the debate.</b> The CG will have just done this. Identify <i>different</i> concepts. Argue why your concepts <i>are more relevant than</i> the CG's concepts in understanding the competing claims. The concepts you identify should then serve as the main points of your speech.</li>
<li><b>Sell your contribution.</b> Explain how your team's position relates to other important arguments in the debate.</li>
<li><b>Avoid making new arguments.</b> You may, however, add details or examples in support of previous claims.</li>
