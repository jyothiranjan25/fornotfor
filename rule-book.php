		<ul class="objectives">
			<li>The third-edition of For Not-For International Debate Competition will take place in a hybrid format wherein the Preliminary rounds shall be online and the following rounds, namely the Pre-Quarters, Quarters, Semi-finals and the Final round will be held offline at the Vijaybhoomi University campus located in Karjat, Maharashtra.</li>
			<li>The schedule for the Competition, along with the format and the structure of the debate can be found in the official website - <a href="https://vijaybhoomi.edu.in/fornotfor/">https://vijaybhoomi.edu.in/fornotfor/</a></li>
			<li>The Competition shall consist of 5 rounds -  a Preliminary round (Online) followed by the Pre-Quarter finals, the Quarter finals, the Semi-finals and the Final round.</li>
        </ul>
		<h3>About British Parliamentary Format :</h3> 
		<ul class="objectives">
			<li>British Parliamentary debate or BP, is the most popular style of intercollegiate debating in the world. It is the format used at the annual World Universities Debating Championship (WUDC). Some people refer to BP as “Worlds” or “WUDC format”.</li>
			<li>The tournament will follow a British Parliamentary Debate format where there shall be 4 teams participating in one round with two Speakers in each team. Two teams represent the Government (in support of the motion), and two teams represent the Opposition (opposed to the Government and motion). Participants will be informed of their team’s position in a given round just before the motion is announced. </li>
			<li>Here are the general names of the teams and the names of the individual roles: <br> <br>
			<div class=" table-responsive">
			     <table class="table table-bordered table-striped">
			         <tbody>
			             <tr>
						     <td><b>Opening Government (OG)</b><br>Prime Minister & Deputy Prime Minister</td>
